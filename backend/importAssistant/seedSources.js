function source(name, baseUrl, {
  sourceType = "html_needs_review",
  providerKey = "custom",
  importMode = "manual",
  sourceGroup = "manual_url",
  parserType = "manual",
  trustLevel = "needs_review",
  categoryFilter = "",
  countryFilter = "",
  enabled = false,
  frequencyMinutes = 360,
  notes = ""
} = {}) {
  return {
    name,
    provider_key: providerKey,
    source_type: sourceType,
    import_mode: importMode,
    source_url: baseUrl,
    base_url: baseUrl,
    enabled,
    is_active: enabled,
    crawl_frequency_minutes: frequencyMinutes,
    crawl_frequency_hours: Math.max(0, Math.round(frequencyMinutes / 60)),
    country_filter: countryFilter,
    category_filter: categoryFilter,
    profession_filter: "",
    source_group: sourceGroup,
    parser_type: parserType,
    parser_config: {},
    trust_level: trustLevel,
    is_editable_by_admin: true,
    original_source_required: true,
    moderation_required: true,
    notes,
    failure_count: 0
  };
}

